package com.emindsoft.zsj.dataStatistics.vo;

import java.util.List;

import com.emindsoft.zsj.base.attachment.model.Attachment;
import com.emindsoft.zsj.dataStatistics.model.Statistics;

public class StatisticsDetailVO {
	private Statistics statistics;
	private List<Attachment> attachment;
	
	public StatisticsDetailVO(Statistics statistics,List<Attachment> attachment){
		this.statistics = statistics;
		this.attachment = attachment;
	}

	public Statistics getStatistics() {
		return statistics;
	}

	public void setStatistics(Statistics statistics) {
		this.statistics = statistics;
	}

	public List<Attachment> getAttachment() {
		return attachment;
	}

	public void setAttachment(List<Attachment> attachment) {
		this.attachment = attachment;
	}
	
	

}
