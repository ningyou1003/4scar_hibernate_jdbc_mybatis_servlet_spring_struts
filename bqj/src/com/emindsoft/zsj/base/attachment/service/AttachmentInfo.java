package com.emindsoft.zsj.base.attachment.service;

import java.io.Serializable;
import java.util.Date;

/**
 * Created by ym on 15-4-17.
 */
public class AttachmentInfo implements Serializable {

	private String id; // 附件id
	private String name; // 原始文件名
	private String url; // 文件下载url
	private String path; // 文件保存路径
	private Date date; // 文件上传时间
	private String relateType;// 文件关联类型
	private String msg;//文件描述

	private String onlineEdit;

	public String getRelateType() {
		return relateType;
	}

	public void setRelateType(String relateType) {
		this.relateType = relateType;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getOnlineEdit() {
		return onlineEdit;
	}

	public void setOnlineEdit(String onlineEdit) {
		this.onlineEdit = onlineEdit;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

}
