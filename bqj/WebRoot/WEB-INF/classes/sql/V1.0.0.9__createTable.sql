CREATE TABLE `c_case` (
  `KeyId` varchar(50) NOT NULL,
  `Title` varchar(255) DEFAULT NULL,
  `Time` datetime DEFAULT NULL,
  `Type` varchar(50) DEFAULT NULL,
  `Creator` varchar(50) DEFAULT NULL,
  `Content` text,
  `url` varchar(255) DEFAULT NULL,
