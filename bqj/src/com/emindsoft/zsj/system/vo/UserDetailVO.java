package com.emindsoft.zsj.system.vo;

import java.util.List;

import com.emindsoft.zsj.system.model.Role;
import com.emindsoft.zsj.system.model.User;

public class UserDetailVO {
	private User user;
	private Role role;	
	private List<Role> roleList;
	
	public UserDetailVO(User user, List<Role> roleList) {
		super();
		this.user = user;
		this.roleList = roleList;
	}
	public UserDetailVO(User user, Role role) {
		super();
		this.user = user;
		this.role = role;
	}
	public UserDetailVO(User user, Role role, List<Role> roleList) {
		super();
		this.user = user;
		this.role = role;
		this.roleList = roleList;
	}

	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	public Role getRole() {
		return role;
	}
	public void setRole(Role role) {
		this.role = role;
	}
	public List<Role> getRoleList() {
		return roleList;
	}
	public void setRoleList(List<Role> roleList) {
		this.roleList = roleList;
	}

}
